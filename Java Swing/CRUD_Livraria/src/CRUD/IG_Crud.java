    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
        BD_conexao x = new BD_conexao();
        ResultSet rs = null;
        x.conectar();

        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        String sql = "SELECT codigo,titulo,autor,genero, preco FROM livros ORDER BY titulo";
        rs = x.buscaLivro(sql);
        try {
            while (rs.next()) {
                dtm.addRow(new Object[]{
                    rs.getInt("codigo"),
                    rs.getString("titulo"),
                    rs.getString("autor"),
                    rs.getString("genero"),
                    rs.getDouble("preco")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(IG_Crud.class.getName()).log(Level.SEVERE, null, ex);
        }
        x.desconectar();
    }//GEN-LAST:event_btnConsultaActionPerformed

    private void btnExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluiActionPerformed
        BD_conexao x = new BD_conexao();
        ResultSet rs = null;
        x.conectar();

        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        int column = 0;
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada!");
            return;
        }
        String value = table.getModel().getValueAt(row, column).toString();
        String sql = "DELETE FROM livros WHERE codigo=" + value;
        rs = x.buscaLivro(sql);
        try {
            if (table.getSelectedRowCount() >= 1) {
                dtm.removeRow(table.getSelectedRow());
                JOptionPane.showMessageDialog(this, "Deleção realizada com sucesso!");
            }
        } catch (HeadlessException ex) {
            Logger.getLogger(IG_Crud.class.getName()).log(Level.SEVERE, null, ex);
        }

        x.desconectar();
    }//GEN-LAST:event_btnExcluiActionPerformed

    private void btnAtualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizaActionPerformed
        BD_conexao x = new BD_conexao();
        x.conectar();
        try {
            if (table.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada!");
                this.setVisible(true);
                return;
            } else {
                this.setVisible(false);
                IG_Atualizar frame = new IG_Atualizar();
                frame.setVisible(true);
                DefaultTableModel dtm = (DefaultTableModel) table.getModel();
            }
        } catch (HeadlessException e) {
            System.out.println("Erro" + e.getMessage());
        }
        x.desconectar();
    }//GEN-LAST:event_btnAtualizaActionPerformed

