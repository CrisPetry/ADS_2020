    private void btnCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastraActionPerformed
        BD_conexao x = new BD_conexao();
        int c = Integer.parseInt(cxCod.getText());
        String t = cxTitle.getText();
        String a = cxAutor.getText();
        String g = cxGen.getText();
        double p = Double.parseDouble(cxPrice.getText());

        x.conectar();
        x.cadastraLivro(c, t, a, g, p);
        JOptionPane.showMessageDialog(this, "Cadastro feito com sucesso!");
        cxCod.setText("");
        cxTitle.setText("");
        cxAutor.setText("");
        cxGen.setText("");
        cxPrice.setText("");
        x.desconectar();
    }//GEN-LAST:event_btnCadastraActionPerformed

